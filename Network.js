// 1 <= n <= 200
// 

function solution(n, computers) {
  for(let i in computers){
    for(let j=0; j<n; j++) {
      if(i===j) continue
      
    }
  }
}




console.log(solution(3, [
                          [1, 1, 0],
                          [1, 1, 0],
                          [0, 0, 1]
                        ]))
